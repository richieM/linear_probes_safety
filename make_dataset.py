# ── Assemble and split ────────────────────────────────────────────────────────

def make_dataset(seed: int = 42) -> dict:
    rng = random.Random(seed)

    def make_split(examples: list[str], label: int, category: str) -> list[dict]:
        shuffled = examples[:]
        rng.shuffle(shuffled)
        n_train = int(0.8 * len(shuffled))
        return [
            {"text": t, "label": label, "category": category,
             "split": "train" if i < n_train else "test"}
            for i, t in enumerate(shuffled)
        ]

    data = []
    data += make_split(HARMFUL_JAILBREAKS,    1, "jailbreak")
    data += make_split(HARMFUL_WEAPONS,        1, "weapons")
    data += make_split(HARMFUL_CYBERCRIME,     1, "cybercrime")
    data += make_split(HARMFUL_MANIPULATION,   1, "manipulation")
    data += make_split(BENIGN_AI_SAFETY,       0, "ai_safety")
    data += make_split(BENIGN_CHEMISTRY_SAFETY,0, "chemistry")
    data += make_split(BENIGN_CYBERSECURITY,   0, "cybersecurity")
    data += make_split(BENIGN_PSYCHOLOGY,      0, "psychology")

    rng.shuffle(data)

    train = [d for d in data if d["split"] == "train"]
    test  = [d for d in data if d["split"] == "test"]

    print(f"Dataset: {len(data)} total  |  train {len(train)}  |  test {len(test)}")
    print(f"  Harmful  — train {sum(1 for x in train if x['label']==1)}, "
          f"test {sum(1 for x in test if x['label']==1)}")
    print(f"  Benign   — train {sum(1 for x in train if x['label']==0)}, "
          f"test {sum(1 for x in test if x['label']==0)}")

    return {"train": train, "test": test}


